import mongoose from 'mongoose'

const UsersSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        default: 'user'
    }
})

export const Users = mongoose.model('Users', UsersSchema)